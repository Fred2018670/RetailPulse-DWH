-- 1. Dynamic Monthly Revenue Performance & Growth Analytics
CREATE OR ALTER PROCEDURE usp_MonthlyRevenue
    @TargetYear INT
AS
BEGIN
    SET NOCOUNT ON;

    -- Using a CTE to compute sequential positions for MoM variance calculations
    WITH MonthlyMetrics AS (
        SELECT 
            d.MonthNumber,
            d.MonthName,
            SUM(fs.NetRevenueUSD) AS NetRevenue_USD,
            SUM(fs.ProfitUSD) AS TotalProfit_USD
        FROM FactSales fs
        JOIN DimDate d ON fs.DateKey = d.DateKey
        JOIN DimStatus s ON fs.StatusKey = s.StatusKey
        WHERE d.[Year] = @TargetYear
          AND s.StatusName NOT IN ('Cancelled', 'Refunded')
        GROUP BY d.MonthNumber, d.MonthName
    )
    SELECT 
        MonthName,
        NetRevenue_USD,
        TotalProfit_USD,
        -- Window function to calculate comparative growth metrics automatically
        ROUND(NetRevenue_USD - LAG(NetRevenue_USD, 1) OVER (ORDER BY MonthNumber), 2) AS AbsoluteGrowth_USD,
        ROUND(((NetRevenue_USD - LAG(NetRevenue_USD, 1) OVER (ORDER BY MonthNumber)) / 
               NULLIF(LAG(NetRevenue_USD, 1) OVER (ORDER BY MonthNumber), 0)) * 100, 2) AS GrowthRatePercentage
    FROM MonthlyMetrics
    ORDER BY MonthNumber;
END;
GO


-- 2. Customer Profile Value Deep-Dive (Instant CRM integration mockup)
CREATE OR ALTER PROCEDURE usp_CustomerLifetimeValue
    @OperationalCustomerID INT
AS
BEGIN
    SET NOCOUNT ON;

    -- Directly isolates specific operational keys while aggregating warehouse metrics
    SELECT 
        c.CustomerID,
        c.CustomerName,
        c.CustomerSegment,
        loc.Country AS DemographyCountry,
        COUNT(DISTINCT fs.OrderID) AS TotalOrdersPlaced,
        SUM(fs.Quantity) AS TotalItemsPurchased,
        SUM(fs.NetRevenueUSD) AS HistoricalSpend_USD,
        SUM(fs.ProfitUSD) AS NetContributionProfit_USD
    FROM FactSales fs
    JOIN DimCustomer c ON fs.CustomerKey = c.CustomerKey
    JOIN DimLocation loc ON c.LocationKey = loc.LocationKey
    WHERE c.CustomerID = @OperationalCustomerID
      AND c.IsCurrent = 1 -- Only pull active SCD profile snapshot
    GROUP BY c.CustomerID, c.CustomerName, c.CustomerSegment, loc.Country;
END;
GO


-- 3. Top Performing Catalog Products Evaluator
CREATE OR ALTER PROCEDURE usp_TopProducts
    @TopN INT
AS
BEGIN
    SET NOCOUNT ON;

    -- Dynamically restrains row generation counts based on consumer parameters
    SELECT TOP (@TopN)
        p.ProductName,
        p.Category,
        p.Brand,
        SUM(fs.Quantity) AS TotalUnitsSold,
        SUM(fs.NetRevenueUSD) AS GeneratedRevenue_USD,
        SUM(fs.ProfitUSD) AS NetProfit_USD
    FROM FactSales fs
    JOIN DimProduct p ON fs.ProductKey = p.ProductKey
    JOIN DimStatus s ON fs.StatusKey = s.StatusKey
    WHERE s.StatusName = 'Completed'
    GROUP BY p.ProductName, p.Category, p.Brand
    ORDER BY NetProfit_USD DESC;
END;
GO

