-- 4. Inventory Turnover Analytics by Product Category
CREATE OR ALTER PROCEDURE usp_InventoryTurnover
    @TargetCategory VARCHAR(100)
AS
BEGIN
    SET NOCOUNT ON;

    -- Financial reporting logic: Cost of Goods Sold (COGS) vs Average Inventory Asset Value
    SELECT 
        p.Category,
        p.SubCategory,
        p.ProductName,
        SUM(fi.StockSold) AS TotalUnitsSold,
        -- COGS = Units Sold * Standard Product Cost
        ROUND(SUM(fi.StockSold * p.StandardCost), 2) AS COGS_USD,
        -- Average Inventory = (Opening Stock + Closing Stock) / 2
        ROUND(AVG((fi.OpeningStock + fi.ClosingStock) / 2.0 * p.StandardCost), 2) AS AverageInventoryValue_USD,
        -- Turnover Ratio = COGS / Average Inventory Value
        ROUND(
            SUM(fi.StockSold * p.StandardCost) / 
            NULLIF(AVG((fi.OpeningStock + fi.ClosingStock) / 2.0 * p.StandardCost), 0), 
            2
        ) AS InventoryTurnoverRatio
    FROM FactInventory fi
    JOIN DimProduct p ON fi.ProductKey = p.ProductKey
    WHERE p.Category = @TargetCategory
    GROUP BY p.Category, p.SubCategory, p.ProductName
    ORDER BY InventoryTurnoverRatio DESC;
END;
GO


-- 5. Deep-Dive Marketing Campaign ROI Optimization Engine
CREATE OR ALTER PROCEDURE usp_MarketingCampaignROI
    @ChannelFilter VARCHAR(50)
AS
BEGIN
    SET NOCOUNT ON;

    -- Combines structural ad costs from FactMarketing with actual attribution conversions
    SELECT 
        c.CampaignName,
        c.Channel,
        SUM(fm.Impressions) AS Impressions,
        SUM(fm.Clicks) AS Clicks,
        ROUND((CAST(SUM(fm.Clicks) AS DECIMAL(18,2)) / NULLIF(SUM(fm.Impressions), 0)) * 100, 3) AS CTR_Percentage, -- Click-Through Rate
        SUM(fm.Conversions) AS Conversions,
        SUM(fm.Cost) AS TotalCampaignCost_USD,
        -- Pulling true matched sales value linked to this campaign from FactSales
        ISNULL(SUM(fs.NetRevenueUSD), 0) AS AttributionRevenue_USD,
        -- ROI Calculation = (Attribution Revenue - Campaign Cost) / Campaign Cost
        ROUND(
            (ISNULL(SUM(fs.NetRevenueUSD), 0) - SUM(fm.Cost)) / NULLIF(SUM(fm.Cost), 0), 
            2
        ) AS ReturnOnInvestmentRatio
    FROM FactMarketing fm
    JOIN DimCampaign c ON fm.CampaignKey = c.CampaignKey
    LEFT JOIN FactSales fs ON fs.CampaignKey = c.CampaignKey AND fs.DateKey = fm.DateKey
    WHERE c.Channel = @ChannelFilter
    GROUP BY c.CampaignName, c.Channel
    ORDER BY ReturnOnInvestmentRatio DESC;
END;
GO

